#!/usr/bin/env python3
"""
Simple check of how digests are generated
"""

import os
import sys
import django
from datetime import date, timedelta

# Setup Django
os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'lecturebuzz.settings')
sys.path.append('.')
django.setup()

from accounts.models import User
from reminders.models import Reminder

def check_digest_generation():
    """Check how digests are currently generated"""
    
    print("🔍 DIGEST GENERATION CHECK")
    print("=" * 30)
    
    today = date.today()
    
    # Get all digests for today
    all_digests = Reminder.objects.filter(
        reminder_type='daily_digest',
        digest_date=today
    ).order_by('created_at')
    
    print(f"📝 Total digests for today: {all_digests.count()}")
    
    if all_digests.exists():
        print(f"\n📋 Digest creation details:")
        
        creation_times = []
        
        for digest in all_digests:
            creation_time = digest.created_at
            creation_times.append(creation_time)
            
            print(f"   👤 {digest.student.username}:")
            print(f"      Created: {creation_time.strftime('%I:%M %p')} UTC")
            print(f"      Scheduled: {digest.reminder_time}")
            print(f"      Is sent: {digest.is_sent}")
        
        # Check if all created at same time (batch) or different times (individual)
        unique_times = len(set(creation_times))
        
        print(f"\n📊 Creation pattern:")
        if unique_times == 1:
            print("   ✅ All digests created at SAME time")
            print("   📍 Generated by: Background service or batch process")
            print("   🤖 This means: AUTOMATIC generation")
        else:
            print("   ⚠️  Digests created at DIFFERENT times")
            print("   📍 Generated by: Website visits or individual triggers")
            print("   🌐 This means: Manual/visit-triggered generation")
    
    else:
        print("📭 No digests found for today")

