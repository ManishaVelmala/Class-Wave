def check_background_service_generation():
    """Check if background service generates digests"""
    
    print(f"\n🤖 BACKGROUND SERVICE GENERATION TEST")
    print("=" * 40)
    
    tomorrow = date.today() + timedelta(days=1)
    
    # Count digests before
    digests_before = Reminder.objects.filter(
        reminder_type='daily_digest',
        digest_date=tomorrow
    ).count()
    
    print(f"📝 Digests before service: {digests_before}")
    
    # Run background service for tomorrow
    from django.core.management import call_command
    
    try:
        print("🤖 Running background service...")
        call_command('send_real_daily_digests', date=tomorrow.isoformat())
        
        # Count digests after
        digests_after = Reminder.objects.filter(
            reminder_type='daily_digest',
            digest_date=tomorrow
        ).count()
        
        print(f"📝 Digests after service: {digests_after}")
        
        if digests_after > digests_before:
            print("✅ Background service DOES generate digests")
            print(f"   Generated: {digests_after - digests_before} new digests")
        else:
            print("❌ Background service does NOT generate new digests")
            
    except Exception as e:
        print(f"❌ Background service error: {e}")

